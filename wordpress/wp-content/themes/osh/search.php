    any Nominee more than once.</p><p>5. How to Obtain Entries</p><p>If you have
    successfully completed the Registration for the Contest during the applicable Prize Pool Entry Window (see section 4
    above), Entrant or Nominee, as applicable, will be granted one (1) entry into the relevant Prize Pool. For sake of
    clarity, the Entrant’s successful Registration for the Contest will either: a) grant the Entrant one (1) entry into
    the Contest; OR b) grant the Nominee one (1) entry into the Contest, as applicable. Entrant may only complete the
    Registration ONCE. Despite the foregoing, Entrant (or Nominee) may be entered multiple times into the Contest
    provided that each entry for the Entrant (or Nominee) is entered by a different person and provided that the Entrant
    (or Nominee) does not enter him or herself into the Contest more than once. Further, no person, whether an Entrant
    or a Nominee, may be entered into a Prize Pool other than his or her applicable Home Prize Pool.</p><p
