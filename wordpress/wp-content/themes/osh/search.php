    Entrant may, instead of entering him or herself into his or her Home Prize Pool, enter one (1) consenting friend or
    family member (a “Nominee”) into the Prize Pool tied to the Carter’s|OshKosh Store closest in proximity to the
    Nominee’s place of residence at the time of entry (similarly, a Nominee’s “Home Prize Pool”). Entrant may only enter
    a Nominee into one (1) Prize Pool: said Nominee’s Home Prize Pool. For sake of clarity, Entrant may either enter
    himself or herself into the Contest OR enter one (1) Nominee into the Contest.</p><p>For example, if
    an Entrant lives in North Bay, Entrant may: a) enter himself or herself into his or her Home Prize Pool (i.e. the
    North Bay Prize Pool) OR b) enter a Nominee into the Nominee’s Home Prize Pool.</p><p>2)
    Registration Process</p><p>(i) Regardless of whether Entrant wishes to be eligible to win a Prize
    Package or wishes to nominate a Nominee to be eligible to win a Prize Package, the Entrant must register by
    completing the following steps:</p>
<ol type="a">
    <li><p>Visit www.cartersoshkosh.ca/carterscomingsoon (the “Contest Page”) during the Contest Period
            and follow the on-screen instructions which will require you to: (i) provide your first and last name, email
            address, and mailing address; and (ii) agree to be legally bound by the terms and conditions of these Rules
            and Regulations.</p></li>
    <li><p>If you wish to enter yourself into your Home Prize Pool, select the proper Carter’s
            Store|OshKosh location from the drop-down menu and then skip to step 2(i)(d) below.</p></li>
    <li><p>If you instead wish to enter a Nominee into a Prize Pool, and you have their explicit consent
            to do so, select the Nominee’s Home Prize Pool from the drop-down menu. You will then be prompted to provide
            the Nominee’s name. You can ONLY enter one (1) Nominee and you can only enter the Nominee into one (1) Prize
            Pool: the Nominee’s Home Prize Pool. Proceed to step 2(i)(d) below.</p></li>
    <li><p>Once you have fully completed the registration form with all required information, click the
            “Submit” button to complete the registration (“Registration”). For an Entrant or a Nominee to be eligible
            for a Prize Package, the relevant Registration must be fully completed, submitted, and received during the
            applicable Prize Pool “Entry Window”: each Prize Pool’s Entry Window is defined as the period between said
            Prize Pool’s Opening Date and Closing Date (see the table in section 3 above). For example, if you live in
            North Bay and you wish to enter into your Home Prize Pool (North Bay), you must submit your completed
            Registration between 9:00 AM ET on July 18, 2016 (the North Bay Opening Date) and 11:59 PM ET on September
            11, 2016 (the North Bay Closing Date). You only need to register one (1) time in this Contest, and you may
            only register once for the Contest.</p></li>
</ol><p>(ii) While not required to complete your Registration, you will be invited to opt-in to the
    Sponsor’s email database (and thereby agree to receive future marketing materials, newsletters, and/or Sponsor
